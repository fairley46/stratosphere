function decompositionReport(result: DecompositionResult): string {
  const lines: string[] = [];
  lines.push("# Decomposition Report");
  lines.push("");

  for (const rec of result.recommendations) {
    lines.push(`## ${rec.componentName}`);
    lines.push(`- Kind: ${rec.kind}`);
    lines.push(`- Confidence: ${rec.confidence}`);
    lines.push(`- Ports: ${rec.ports.length > 0 ? rec.ports.join(", ") : "none"}`);
    lines.push(`- Dependencies: ${rec.dependencies.length > 0 ? rec.dependencies.join(", ") : "none"}`);
    lines.push("- Rationale:");
    for (const rationale of rec.rationale) {
      lines.push(`  - ${rationale}`);
    }
    lines.push("");
  }

  if (result.blockers.length > 0) {
    lines.push("## Blockers");
    for (const blocker of result.blockers) {
      lines.push(`- ${blocker}`);
    }
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

function vmDnaReport(discovery: DiscoveryResult): string {
  return JSON.stringify(discovery, null, 2);
}

function addArtifact(artifacts: GeneratedArtifact[], path: string, content: string): void {
  artifacts.push({ path, content });
}

export function generateArtifacts(
  migrationId: string,
  discovery: DiscoveryResult,
  decomposition: DecompositionResult
): ArtifactBundle {
  const artifacts: GeneratedArtifact[] = [];

  for (const component of decomposition.recommendations) {
    addArtifact(artifacts, `docker/${component.componentId}/Dockerfile`, dockerfileFor(component));
  }

  addArtifact(artifacts, "helm/Chart.yaml", chartYaml(migrationId));
  addArtifact(artifacts, "helm/values.yaml", valuesYaml(decomposition.recommendations));
  addArtifact(artifacts, "helm/templates/workloads.yaml", workloadsTemplate());
  addArtifact(artifacts, "helm/templates/services.yaml", servicesTemplate());

  for (const provider of ["aws", "azure", "gcp", "openstack"] as const) {
    addArtifact(artifacts, `terraform/${provider}/main.tf`, terraformMain(provider));
  }

  addArtifact(artifacts, "reports/decomposition.md", decompositionReport(decomposition));
  addArtifact(artifacts, "reports/vm-dna.json", vmDnaReport(discovery));

  return { artifacts };
}
