function defaultPort(component: WorkloadRecommendation): number {
  return component.ports[0] ?? 8080;
}

function dockerBaseFor(stack: StackType): { image: string; packageCmd: string } {
  if (stack === "java-spring") {
    return {
      image: "eclipse-temurin:21-jre-alpine",
      packageCmd: "apk add --no-cache shadow",
    };
  }

  if (stack === "dotnet") {
    return {
      image: "mcr.microsoft.com/dotnet/aspnet:8.0-alpine",
      packageCmd: "apk add --no-cache shadow",
    };
  }

  if (stack === "nodejs") {
    return {
      image: "node:22-alpine",
      packageCmd: "apk add --no-cache shadow",
    };
  }

  if (stack === "python") {
    return {
      image: "python:3.12-alpine",
      packageCmd: "apk add --no-cache shadow",
    };
  }

  return {
    image: "cgr.dev/chainguard/wolfi-base:latest",
    packageCmd: "apk add --no-cache shadow",
  };
}

function runtimeCommandFor(stack: StackType, component: WorkloadRecommendation): string {
  if (stack === "java-spring") {
    return "CMD [\"sh\", \"-c\", \"java -jar /app/app.jar\"]";
  }

  if (stack === "dotnet") {
    return `CMD ["sh", "-c", "dotnet /app/${component.componentName}.dll"]`;
  }

  if (stack === "nodejs") {
    return "CMD [\"sh\", \"-c\", \"node /app/index.js\"]";
  }

  if (stack === "python") {
    return "CMD [\"sh\", \"-c\", \"python /app/main.py\"]";
  }

  return `CMD ["/bin/sh", "-c", "echo 'override runtime command for ${component.componentName}' && sleep infinity"]`;
}

function dockerfileFor(component: WorkloadRecommendation): string {
  const { image, packageCmd } = dockerBaseFor(component.stack);

  return `# Generated by Stratosphere
FROM ${image}

RUN ${packageCmd} \\
  && groupadd -g 10001 app \\
  && useradd -u 10001 -g app -M -s /sbin/nologin app

WORKDIR /app
COPY . /app

USER app
EXPOSE ${defaultPort(component)}
HEALTHCHECK --interval=30s --timeout=3s --start-period=20s CMD ["sh", "-c", "echo ok"]

# Stack detected: ${component.stack}
${runtimeCommandFor(component.stack, component)}
`;
}

function chartYaml(migrationId: string): string {
  return `apiVersion: v2
name: ${migrationId}-migration
version: 0.1.0
appVersion: "1.0"
description: Generated by Stratosphere migration architect
`;
}

function valuesYaml(recommendations: WorkloadRecommendation[]): string {
  const lines: string[] = [];
  lines.push("global:");
  lines.push("  imagePullPolicy: IfNotPresent");
  lines.push("  podSecurityContext:");
  lines.push("    runAsNonRoot: true");
  lines.push("    runAsUser: 10001");
  lines.push("    runAsGroup: 10001");
  lines.push("    fsGroup: 10001");
  lines.push("  containerSecurityContext:");
  lines.push("    allowPrivilegeEscalation: false");
  lines.push("    readOnlyRootFilesystem: false");
  lines.push("    capabilities:");
  lines.push("      drop: [\"ALL\"]");
  lines.push("");
  lines.push("components:");

  for (const item of recommendations) {
    lines.push(`  - id: ${item.componentId}`);
    lines.push(`    name: ${item.componentName}`);
    lines.push(`    stack: ${item.stack}`);
    lines.push(`    kind: ${item.kind}`);
    lines.push(`    replicas: ${item.kind === "StatefulSet" ? 1 : item.kind === "CronJob" ? 1 : 2}`);
    lines.push(`    image: ${item.imageTag}`);
    lines.push(`    schedule: ${item.schedule ? `"${item.schedule}"` : "\"\""}`);
    lines.push(`    ports: [${item.ports.join(", ")}]`);
    lines.push("    resources:");
    lines.push("      requests:");
    lines.push(`        cpu: ${item.resourceRecommendation.cpuRequestMillicores}m`);
    lines.push(`        memory: ${item.resourceRecommendation.memoryRequestMb}Mi`);
    lines.push("      limits:");
    lines.push(`        cpu: ${item.resourceRecommendation.cpuLimitMillicores}m`);
    lines.push(`        memory: ${item.resourceRecommendation.memoryLimitMb}Mi`);
    lines.push("    probes:");
    lines.push("      readinessPath: /health/ready");
    lines.push("      livenessPath: /health/live");
    lines.push("      startupPath: /health/startup");
    lines.push("    envConfig: []");
    lines.push("    envSecret: []");
    lines.push("    persistence:");
    lines.push(`      enabled: ${item.kind === "StatefulSet"}`);
    lines.push("      size: 20Gi");
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

