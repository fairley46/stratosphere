function decompositionReport(result: DecompositionResult): string {
  const lines: string[] = [];
  lines.push("# Decomposition Report");
  lines.push("");

  for (const recommendation of result.recommendations) {
    lines.push(`## ${recommendation.componentName}`);
    lines.push(`- Kind: ${recommendation.kind}`);
    lines.push(`- Stack: ${recommendation.stack}`);
    lines.push(`- Confidence: ${recommendation.confidence}`);
    lines.push(`- Ports: ${recommendation.ports.length > 0 ? recommendation.ports.join(", ") : "none"}`);
    lines.push(`- Dependencies: ${recommendation.dependencies.length > 0 ? recommendation.dependencies.join(", ") : "none"}`);
    lines.push("- Rationale:");
    for (const rationale of recommendation.rationale) {
      lines.push(`  - ${rationale}`);
    }
    lines.push("");
  }

  if (result.blockers.length > 0) {
    lines.push("## Blockers");
    for (const blocker of result.blockers) {
      lines.push(`- ${blocker}`);
    }
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

function securityReport(recommendations: WorkloadRecommendation[]): string {
  const lines: string[] = [
    "# Security Baseline Notes",
    "",
    "Generated defaults include:",
    "- Non-root container execution",
    "- Dropped Linux capabilities",
    "- Liveness/readiness/startup probes",
    "- ConfigMap/Secret split templates",
    "- Default egress policy template",
    "",
    "## Component-specific Notes",
  ];

  for (const recommendation of recommendations) {
    lines.push(`- ${recommendation.componentName}: stack=${recommendation.stack}, kind=${recommendation.kind}`);
  }

  lines.push("");
  lines.push("Review all generated probes and environment injection before production rollout.");

  return `${lines.join("\n")}\n`;
}

function vmDnaReport(discovery: DiscoveryResult): string {
  return JSON.stringify(discovery, null, 2);
}

function addArtifact(artifacts: GeneratedArtifact[], path: string, content: string): void {
  artifacts.push({ path, content });
}

export function generateArtifacts(
  migrationId: string,
  discovery: DiscoveryResult,
  decomposition: DecompositionResult
): ArtifactBundle {
  const artifacts: GeneratedArtifact[] = [];

  for (const component of decomposition.recommendations) {
    addArtifact(artifacts, `docker/${component.componentId}/Dockerfile`, dockerfileFor(component));
  }

  addArtifact(artifacts, "helm/Chart.yaml", chartYaml(migrationId));
  addArtifact(artifacts, "helm/values.yaml", valuesYaml(decomposition.recommendations));
  addArtifact(artifacts, "helm/templates/serviceaccount.yaml", serviceAccountTemplate());
  addArtifact(artifacts, "helm/templates/config.yaml", configTemplates());
  addArtifact(artifacts, "helm/templates/workloads.yaml", workloadsTemplate());
  addArtifact(artifacts, "helm/templates/services.yaml", servicesTemplate());
  addArtifact(artifacts, "helm/templates/networkpolicy.yaml", networkPolicyTemplate());

  for (const provider of ["aws", "azure", "gcp", "openstack"] as const) {
    addArtifact(artifacts, `terraform/${provider}/main.tf`, terraformMain(provider));
    addArtifact(artifacts, `terraform/${provider}/variables.tf`, terraformVariables(provider));
    addArtifact(artifacts, `terraform/${provider}/outputs.tf`, terraformOutputs());
    addArtifact(artifacts, `terraform/${provider}/README.md`, terraformReadme(provider));
  }

  addArtifact(artifacts, "reports/decomposition.md", decompositionReport(decomposition));
  addArtifact(artifacts, "reports/security-baseline.md", securityReport(decomposition.recommendations));
  addArtifact(artifacts, "reports/vm-dna.json", vmDnaReport(discovery));

  return { artifacts };
}
