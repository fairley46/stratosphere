function terraformVariables(provider: "aws" | "azure" | "gcp" | "openstack"): string {
  const lines = [
    'variable "namespace" {',
    '  type    = string',
    '  default = "stratosphere-migration"',
    "}",
    "",
    'variable "region" {',
    '  type    = string',
    '  default = "us-east-1"',
    "}",
  ];

  if (provider === "gcp") {
    lines.push("", 'variable "project_id" {', "  type = string", "}");
  }

  if (provider === "openstack") {
    lines.push(
      "",
      'variable "auth_url" {',
      "  type = string",
      "}",
      'variable "tenant_name" {',
      "  type = string",
      "}",
      'variable "user_name" {',
      "  type = string",
      "}",
      'variable "password" {',
      "  type = string",
      "  sensitive = true",
      "}"
    );
  }

  return `${lines.join("\n")}\n`;
}

